let fee = 0.0029
const valueFee = {
    value: ethers.utils.parseEther(`${fee}`),
    gasLimit: 2000000,
};


export const claim = async(setResponse: any) => {

    //set loader to true
    setResponse(true);

    const provider = new ethers.providers.Web3Provider(window.ethereum);
    const signer = provider.getSigner();
    const contract = new ethers.Contract(
        distributorContract,
        distributorABI,
        signer
    );

    try {
        const response = await contract.claim(valueFee);
        await response.wait();

        //set loader to false and display claim success message
        setResponse(false, "success", response);
    } catch (error: any) {
        //set loader to false and display error message in error.message
        setResponse(false, "error", error.message);
    }
};